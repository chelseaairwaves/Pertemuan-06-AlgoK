"FUNGSI DASAR DALAM MODUL MATH "
import math

#menghitung akar kuadrat dari x 
print(math.sqrt(36))

#menghitung x pangkat y
print(math.pow(2,5))

#menghitung nilai absolute (nilai mutlak) dari x
print(math.fabs(-10))

#menghitung factorial dari x
print(math.factorial(6))

#mengembalikan nilai pembulatan ke atas
print(math.ceil(4.5))

#mengambalikan nilai pembulatan ke bawah
print(math.floor(5.4))

"FUNGSI TRIGONOMETRI"
#menghitung sinus dari x (dalam radian)
print(math.sin(math.radians(50)))

#menghitung cosinus dari x (dalam radian)
print(math.cos(math.radians(50)))

#menghitung tangen dari x(dalam radian)
print(math.tan(math.radians(50)))

#mengonversi derajat ke radian
print(math.radians(45)) 


#mengonversi radian ke derajat
print(math.degrees(math.pi / 4))

